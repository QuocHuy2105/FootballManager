    private void btnChonDoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonDoiActionPerformed
        MainFrame mainFrame = (MainFrame) SwingUtilities.getWindowAncestor(this);
        ChonDoiDialog dialog = new ChonDoiDialog(mainFrame, true, isSelectedTeam, idDoiNha, idDoiKhach);
        dialog.setVisible(true);
        
        if(!dialog.isConfirm()) return;
        
        try {
            MatchTeamService mtService = new MatchTeamService();
            MatchPlayerService mpService = new MatchPlayerService();
            TeamService service = new TeamService();
            int homeTeamId = dialog.getHomeTeamId();
            int awayTeamId = dialog.getAwayTeamId();
            if(!isSelectedTeam){
                mtService.addMatchTeam(new MatchTeams(matchId, homeTeamId, TeamRole.HOME));
                mtService.addMatchTeam(new MatchTeams(matchId, awayTeamId, TeamRole.AWAY));
            }
            else {
                
                mtService.deleteById(matchId, idDoiNha);
                mtService.deleteById(matchId, idDoiKhach);

                mtService.addMatchTeam(new MatchTeams(matchId, awayTeamId, TeamRole.AWAY));
                mtService.addMatchTeam(new MatchTeams(matchId, homeTeamId, TeamRole.HOME));
            }
            lbTenDoiNha.setText(service.getTeamById(homeTeamId).getTeamName());
            lbTenDoiKhach.setText(service.getTeamById(awayTeamId).getTeamName());
            if(isSelectedTeam){
                if(idDoiNha != homeTeamId) mpService.delete(doiNha);
                if(idDoiKhach != awayTeamId) mpService.delete(doiKhach);
            }
            idDoiNha = homeTeamId;
            idDoiKhach = awayTeamId;
            isSelectedTeam = true;
            loadTableDoiNha();
            loadTableDoiKhach();
            setBtnVisibility();
            deleteAllEvents();
        } catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnChonDoiActionPerformed

    private void btnChonCauThuDoiNhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonCauThuDoiNhaActionPerformed
        MainFrame mainFrame = (MainFrame) SwingUtilities.getWindowAncestor(this);
        ChonDoiHinhDialog dialog = new ChonDoiHinhDialog(mainFrame, true, idDoiNha, isSelectedPlayerHome);
        dialog.setVisible(true);
        
        if(!dialog.isConfirm()) return;
        
        try{
            List<Player> daChinh = dialog.getStartingList();
            List<Player> duBi = dialog.getSubList();
            MatchPlayerService mpService = new MatchPlayerService();
            if(isSelectedPlayerHome){
                 mpService.delete(doiNha);
            }
            
            for(Player player : daChinh) mpService.add(new MatchPlayers(matchId, player.getPlayerId(), PlayerRole.STARTING, player.getPosition()));
            for(Player player : duBi) mpService.add(new MatchPlayers(matchId, player.getPlayerId(), PlayerRole.SUBSTITUTE, player.getPosition()));
            
            loadTableDoiNha();
            deleteAllEvents();
            
        } catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnChonCauThuDoiNhaActionPerformed

