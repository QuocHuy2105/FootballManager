    private void loadTableDoiNha(){
        try {
            MatchPlayerService service = new MatchPlayerService();
            PlayerService pService = new PlayerService();
            doiNha = service.getByMatchAndTeam(matchId, idDoiNha);
            modelDoiNha.setRowCount(0);
            if(doiNha.size() == 11){
                isSelectedPlayerHome = true;
                for(MatchPlayers mp : doiNha){
                    Player player = pService.getPlayerById(mp.getPlayerId());
                    String role = mp.getPlayerRole().equals(PlayerRole.STARTING) ? "Đá chính" : "Dự bị";
                    modelDoiNha.addRow(new Object[]{player.getPlayerName(), mp.getPosition().name(), role});
                }
            }
        } catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void loadTableDoiKhach(){
        try {
            MatchPlayerService service = new MatchPlayerService();
            PlayerService pService = new PlayerService();
            doiKhach = service.getByMatchAndTeam(matchId, idDoiKhach);
            modelDoiKhach.setRowCount(0);
            if(doiKhach.size() == 11){
                isSelectedPlayerAway = true;
                for(MatchPlayers mp : doiKhach){
                    Player player = pService.getPlayerById(mp.getPlayerId());
                    String role = mp.getPlayerRole().equals(PlayerRole.STARTING) ? "Đá chính" : "Dự bị";
                    modelDoiKhach.addRow(new Object[]{player.getPlayerName(), mp.getPosition().name(), role});
                }
            }
        } catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void loadData(){
        try {
            MatchTeamService mtService = new MatchTeamService();
            List<MatchTeams> mtLst = mtService.getTeamOfMatch(matchId);
            
            if(mtLst.size() < 2){
                isSelectedTeam = false;
                lbTenDoiNha.setText(CHUA_XAC_DINH);
                lbTenDoiKhach.setText(CHUA_XAC_DINH);
                return;
            }
            
            isSelectedTeam = true;
            
            for(MatchTeams mt : mtLst){
                if(mt.getTeamRole().equals(TeamRole.HOME)) idDoiNha = mt.getTeamId();
                if(mt.getTeamRole().equals(TeamRole.AWAY)) idDoiKhach = mt.getTeamId();
            }
            
            TeamService tService = new TeamService();
            String tenDoiNha = tService.getTeamById(idDoiNha).getTeamName();
            String tenDoiKhach = tService.getTeamById(idDoiKhach).getTeamName();
            
            lbTenDoiNha.setText(tenDoiNha);
            lbTenDoiKhach.setText(tenDoiKhach);
            
            loadTableDoiNha();
            loadTableDoiKhach();
        } catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void getMatchStatus(){
        try {
            MatchService service = new MatchService();
            isMatchFinish = service.isMatchFinished(matchId);
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
