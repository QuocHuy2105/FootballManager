    private void initTableSelectionListener(){
        tbCauThu.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) { // Đảm bảo chỉ xử lý khi chọn xong
                int selectedCount = tbCauThu.getSelectedRowCount();
                
                btnChon.setEnabled(selectedCount == 1);
            }   
        }); 
    }
    
    private void btnChonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonActionPerformed
        int selectedRow = tbCauThu.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Chọn một cầu thủ để thêm sự kiện");
            return;
        }
        isConfirm = true;
        dispose();
    }//GEN-LAST:event_btnChonActionPerformed

