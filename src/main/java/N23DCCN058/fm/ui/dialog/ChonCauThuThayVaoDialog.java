    public Player getPlayer(){
        int selectedRow = tbCauThu.getSelectedRow();
        int id = (int) tbCauThu.getValueAt(selectedRow, 0);
        return player.get(id);
    }
    
    private void initTable() {
        String[] columnNames = {"Id", "Tên cầu thủ", "Vị trí"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        tbCauThu.setModel(model);
        
        tbCauThu.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        
        TableColumnModel col = tbCauThu.getColumnModel();
        
        col.getColumn(0).setPreferredWidth(40); // Id cầu thủ
        col.getColumn(1).setPreferredWidth(140); // Tên cầu thủ
        col.getColumn(2).setPreferredWidth(100); // Ngày sinh
        
        for (int i = 0; i < col.getColumnCount(); i++) {
            col.getColumn(i).setResizable(false);
        }
        
        for(Map.Entry<Integer, Player> entry : player.entrySet()){
            
            if(!playerOnField.contains(entry.getKey()) && !playerGetRedCard.contains(entry.getKey())){
                Player p = entry.getValue();
                model.addRow(new Object[]{p.getPlayerId(), p.getPlayerName(), positions.get(p.getPlayerId()).name()});
            }
        }
    }
    
