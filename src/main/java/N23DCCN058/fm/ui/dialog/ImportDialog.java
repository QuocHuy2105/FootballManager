    public boolean validateExcelStructure(Sheet sheet, List<String> requiredColumns) {

        Row header = sheet.getRow(0);
        if (header == null) return false;

        int excelColumnCount = header.getLastCellNum();
        int requiredColumnCount = requiredColumns.size();

        // Kiểm tra số lượng cột có đúng hay không
        if (excelColumnCount != requiredColumnCount) {
            return false;
        }

        // Kiểm tra từng tên cột theo đúng thứ tự
        for (int i = 0; i < requiredColumnCount; i++) {
            Cell cell = header.getCell(i);
            String colName = (cell != null) ? cell.getStringCellValue().trim() : "";

            if (!colName.equalsIgnoreCase(requiredColumns.get(i))) {
                return false;
            }
        }

        return true;
    }
    
    public boolean validateTeamExcel(Sheet sheet) {
        List<String> required = Arrays.asList(
            "Tên đội bóng",
            "Phòng ban",
            "Tên huấn luyện viên"
        );

        return validateExcelStructure(sheet, required);
    }
    
