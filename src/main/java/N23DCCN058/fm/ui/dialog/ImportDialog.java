    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(txtFilePath.getText() == null || txtFilePath.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Chọn một file để import!");
            return;
        }
        
        Workbook workbook = null;
        try {
            workbook = new XSSFWorkbook(new FileInputStream(txtFilePath.getText()));
        } catch (FileNotFoundException ex) {
            System.getLogger(ImportDialog.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        } catch (IOException ex) {
            System.getLogger(ImportDialog.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
        Sheet sheet = workbook.getSheetAt(0);

        boolean valid = false;

        switch (cbDuLieu.getSelectedItem().toString()) {
            case "Đội bóng":
                valid = validateTeamExcel(sheet);
                break;

            case "Cầu thủ":
                valid = validatePlayerExcel(sheet);
                break;

            case "Trọng tài":
                valid = validateRefereeExcel(sheet);
                break;
        }

        if (!valid) {
            JOptionPane.showMessageDialog(this,
                "File Excel không đúng cấu trúc đối với loại dữ liệu đã chọn!",
                "Sai cấu trúc",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            TournamentService service = new TournamentService();
            TeamService teamService = new TeamService();
            PlayerService playerService = new PlayerService();
            RefereeService refereeService = new RefereeService();
            List<ErrorDetail> errors = null;
            
            switch (cbDuLieu.getSelectedItem().toString()) {
                case "Đội bóng":
                    List<Team> teams = service.readTeamsFromExcel(txtFilePath.getText());
                    errors = teamService.validateTeams(teams);
                    if(errors.isEmpty()) teamService.addTeams(teams);
                    break;
                case "Cầu thủ":
                    List<Player> players = service.readPlayersFromExcel(txtFilePath.getText());
                    errors = playerService.validatePlayers(players);
                    if(errors.isEmpty()) playerService.addPlayers(players);
                    break;
                case "Trọng tài":
                    List<Referee> referees = service.readRefereesFromExcel(txtFilePath.getText());
                    errors = refereeService.validateReferees(referees);
                    if(errors.isEmpty()) refereeService.addReferees(referees);
            }
            
            if(!errors.isEmpty()){
                String message = "Dữ liệu không hợp lệ: ";
                for(ErrorDetail er : errors){
                    message += "\nDòng " + er.getRowIndex() + ": " + er.getMessage();
                }
                JOptionPane.showMessageDialog(this, message, "Lỗi dữ liệu", JOptionPane.ERROR_MESSAGE);
            }
            else {
                JOptionPane.showMessageDialog(this, "Import thành công!");
                dispose();
            }
            
        
        }catch(Exception e){
            e.printStackTrace();
        }
        
        

    }//GEN-LAST:event_jButton2ActionPerformed

