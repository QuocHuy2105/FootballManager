    private Integer getHomeTeam(){
        String tenDoiBong = (String) cbDoiNha.getSelectedItem();
        int idx;
        for(idx = 0; idx < lstTeam.size(); idx++){
            if(lstTeam.get(idx).getTeamName().equalsIgnoreCase(tenDoiBong)) break;
        }
        return idx >= lstTeam.size() ? null : lstTeam.get(idx).getTeamId();
    }
    
    private Integer getAwayTeam(){
        String tenDoiBong = (String) cbDoiKhach.getSelectedItem();
        int idx;
        for(idx = 0; idx < lstTeam.size(); idx++){
            if(lstTeam.get(idx).getTeamName().equalsIgnoreCase(tenDoiBong)) break;
        }
        return idx >= lstTeam.size() ? null : lstTeam.get(idx).getTeamId();
    }
    
    private void getListTeam(){
        try {
            TeamService service = new TeamService();
            lstTeam = service.getAllTeam();
        } catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void initComboBoxData(){
        cbDoiNha.removeAllItems();
        cbDoiKhach.removeAllItems();
        
        for(Team team: lstTeam){
            cbDoiNha.addItem(team.getTeamName());
            cbDoiKhach.addItem(team.getTeamName());
        }
        
        if(!isSelectedTeam){
            cbDoiNha.setSelectedIndex(-1);
            cbDoiKhach.setSelectedIndex(-1);
        }
        else {
            try {
            TeamService service = new TeamService();
                cbDoiNha.setSelectedItem(service.getTeamById(idHomeTeam).getTeamName());
                cbDoiKhach.setSelectedItem(service.getTeamById(idAwayTeam).getTeamName());
            } catch(Exception e){
                e.printStackTrace();
            }
        }
        
    }
    
    public boolean isConfirm(){
        return isConfirm;
    }
    
