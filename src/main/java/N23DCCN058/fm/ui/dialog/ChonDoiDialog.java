    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Integer newIdHome = getHomeTeam();
        Integer newIdAway = getAwayTeam();
        
        if(newIdHome == null || newIdAway == null || newIdHome <= 0 || newIdAway <= 0){
            JOptionPane.showMessageDialog(this, "Phải chọn đủ hai đội bóng tham gia", "Chưa chọn đủ thông tin", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(newIdHome == newIdAway){
            JOptionPane.showMessageDialog(this, "Không được chọn hai đội giống nhau", "Chưa chọn đủ thông tin", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(newIdHome == idHomeTeam && newIdAway == idAwayTeam) dispose();
        
        this.idHomeTeam = newIdHome;
        this.idAwayTeam = newIdAway;
        
        if(!isSelectedTeam){
            isConfirm = true;
            dispose();
            return;
        }
        
        int choice = JOptionPane.showConfirmDialog(
            this,
            "Đổi đội bóng sẽ xóa toàn bộ danh sách cầu thủ và danh sách sự kiện có liên quan!",
            "Xác nhận cập nhật",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE
        );
        
        if(choice == JOptionPane.YES_OPTION){
            isConfirm = true;
            dispose();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

