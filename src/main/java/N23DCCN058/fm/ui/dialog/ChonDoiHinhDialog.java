    private void btnThemDaChinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemDaChinhActionPerformed
        int selectedRow = tbDanhSach.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 cầu thủ!");
            return;
        }
        
        int id = (int) tbDanhSach.getValueAt(selectedRow, 0);
        Player player = map.get(id);
        
        boolean isGk = player.getPosition().equals(PlayerPosition.GOALKEEPER);
        
        if(totalStarting >= 5){
            JOptionPane.showMessageDialog(this, "Số lượng cầu thủ đá chính không được vượt quá 5!");
            return;
        }
        
        if(isGk && totalGk == 1){
            JOptionPane.showMessageDialog(this, "Chỉ có 1 GoalKeeper được tham gia đá chính");
            return;
        }
        
        if(!isGk && totalNor == 4){
            JOptionPane.showMessageDialog(this, "Cầu thủ còn lại phải là GoalKeeper");
            return;
        }
        
        daChinh.add(player);
        
        totalStarting++;
        if(isGk) totalGk++;
        else totalNor++;
        
        modelDanhSach.removeRow(selectedRow);
        modelDaChinh.addRow(new Object[]{player.getPlayerId(), player.getPlayerName(), player.getPosition().name()});
        
        updateLabelCount();
    }//GEN-LAST:event_btnThemDaChinhActionPerformed

    private void btnThemDuBiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemDuBiActionPerformed
        int selectedRow = tbDanhSach.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 cầu thủ!");
            return;
        }
        
        int id = (int) tbDanhSach.getValueAt(selectedRow, 0);
        Player player = map.get(id);
        
        if(totalSub >= 6){
            JOptionPane.showMessageDialog(this, "Số lượng cầu thủ dự bị không được vượt quá 6!");
            return;
        }
        
        duBi.add(player);
        
        totalSub++;
        
        modelDanhSach.removeRow(selectedRow);
        modelDuBi.addRow(new Object[]{player.getPlayerId(), player.getPlayerName(), player.getPosition().name()});
        
        updateLabelCount();
    }//GEN-LAST:event_btnThemDuBiActionPerformed

