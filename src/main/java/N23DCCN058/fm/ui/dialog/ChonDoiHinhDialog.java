    private void initTableSelectionListener(){
        tbDanhSach.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting() && tbDanhSach.getSelectedRow() != -1) { // Chỉ xử lý khi quá trình chọn kết thúc và có hàng được chọn
                // Bỏ chọn ở hai bảng còn lại
                tbDaChinh.clearSelection(); 
                tbDuBi.clearSelection();

                // Cập nhật trạng thái nút
                updateOnClickDanhSach();
            } else if (!e.getValueIsAdjusting() && tbDanhSach.getSelectedRow() == -1) {
                // Trường hợp không còn hàng nào được chọn sau khi xóa/bỏ chọn
                updateOnClickDanhSach();
            }
        }); 

        // Listener cho tbDaChinh
        tbDaChinh.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting() && tbDaChinh.getSelectedRow() != -1) {
                // Bỏ chọn ở hai bảng còn lại
                tbDanhSach.clearSelection();
                tbDuBi.clearSelection();

                // Cập nhật trạng thái nút
                updateOnClickDaChinh();
            } else if (!e.getValueIsAdjusting() && tbDaChinh.getSelectedRow() == -1) {
                 updateOnClickDaChinh();
            }
        }); 

        // Listener cho tbDuBi
        tbDuBi.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting() && tbDuBi.getSelectedRow() != -1) {
                // Bỏ chọn ở hai bảng còn lại
                tbDanhSach.clearSelection();
                tbDaChinh.clearSelection();

                // Cập nhật trạng thái nút
                updateOnClickDuBi();
            } else if (!e.getValueIsAdjusting() && tbDuBi.getSelectedRow() == -1) {
                 updateOnClickDuBi();
            }
        });
    }
    
    private void updateOnClickDanhSach(){
        
        if (tbDanhSach.getSelectedRow() == -1) { // <-- Thêm kiểm tra này
            btnThemDaChinh.setEnabled(false);
            btnThemDuBi.setEnabled(false);
            btnXoa.setEnabled(false);
            return;
        }
        
        int selectedCount = tbDanhSach.getSelectedRowCount();
        
        boolean enable = (selectedCount == 1);
        boolean isPlayerGk = tbDanhSach.getValueAt(tbDanhSach.getSelectedRow(), 2).toString().equalsIgnoreCase(PlayerPosition.GOALKEEPER.name());
        boolean isOk = isPlayerGk ? totalGk < 1 : totalNor < 5;
        btnThemDaChinh.setEnabled(enable && totalStarting < 5 && isOk);
        btnThemDuBi.setEnabled(enable && totalSub < 6);
        btnXoa.setEnabled(false);
    }
    
    private void updateOnClickDaChinh(){
        if (tbDaChinh.getSelectedRow() == -1) { // <-- Thêm kiểm tra này
            btnThemDaChinh.setEnabled(false);
            btnThemDuBi.setEnabled(false);
            btnXoa.setEnabled(false);
            return;
        }
                
        int selectedCount = tbDaChinh.getSelectedRowCount();
        boolean enable = (selectedCount == 1);
        btnThemDaChinh.setEnabled(false);
        btnThemDuBi.setEnabled(false);
        btnXoa.setEnabled(enable);
    }
    
    private void updateOnClickDuBi(){
        if (tbDuBi.getSelectedRow() == -1) { // <-- Thêm kiểm tra này
            btnThemDaChinh.setEnabled(false);
            btnThemDuBi.setEnabled(false);
            btnXoa.setEnabled(false);
            return;
        }
        
        int selectedCount = tbDuBi.getSelectedRowCount();
        boolean enable = (selectedCount == 1);
        btnThemDaChinh.setEnabled(false);
        btnThemDuBi.setEnabled(false);
        btnXoa.setEnabled(enable);
    }
    
